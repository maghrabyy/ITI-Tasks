var textPar = document.getElementById('PAR')

function ChangeFont(fontFamily){
    textPar.style.fontFamily = fontFamily
}

function ChangeAlign(alignment){
    textPar.style.textAlign = alignment
}

function ChangeHeight(height){
    textPar.style.lineHeight = height
}

function ChangeLSpace(space){
    textPar.style.letterSpacing = space
}

function ChangeIndent(indent){
    textPar.style.textIndent = indent
}

function ChangeTransform(textCase){
    textPar.style.textTransform = textCase
}

function ChangeDecorate(dec){
    textPar.style.textDecoration = dec
}

function ChangeBorder(border){
    textPar.style.border = border
}

function ChangeBorderColor(color){
    textPar.style.borderColor = color
}